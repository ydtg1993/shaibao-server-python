from enum import Enum


class Week(Enum):
    MONDAY = 'Monday'        # 星期一
    TUESDAY = 'Tuesday'      # 星期二
    WEDNESDAY = 'Wednesday'  # 星期三
    THURSDAY = 'Thursday'    # 星期四
    FRIDAY = 'Friday'        # 星期五
    SATURDAY = 'Saturday'    # 星期六
    SUNDAY = 'Sunday'        # 星期天

